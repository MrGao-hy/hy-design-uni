/**
 * 不同主题对应不同的图标
 * */
export const iconName = (type: string) => {
  switch (type) {
    case "success":
      return IconConfig.SUCCESS_FILL;
    case "error":
      return IconConfig.CLOSE_CIRCLE_FILL;
    case "warning":
      return IconConfig.NOTICE_FILL;
    case "info":
      return IconConfig.HELP_FILL;
    case "primary":
      return IconConfig.MESSAGE_FILL;
    default:
      return IconConfig.CLOSE_CIRCLE_FILL;
  }
};
