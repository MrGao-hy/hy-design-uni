<template>
  <text
    :class="['hy-price', customClass]"
    :style="priceStyle"
    @tap="handleClick"
  >
    <text class="hy-price__prefix">{{ symbol }}</text>
    <text
      class="hy-price__text"
      :style="[{ 'font-size': addUnit(getPx(size) * ratio) }]"
    >
      {{ priceOne?.[0] }}
    </text>
    <text class="hy-price__decimal">
      {{ "." + addZero(priceOne?.[1], num) }}
    </text>
  </text>
</template>

<script lang="ts">
export default {
  name: "hy-price",
  options: {
    addGlobalClass: true,
    virtualHost: true,
    styleIsolation: "shared",
  },
};
</script>

<script setup lang="ts">
import { computed } from "vue";
import type { CSSProperties, PropType } from "vue";
import { addUnit, getPx, error, addZero } from "../../utils";

/**
 * 业务组件,突出金额小数点前大，小数点后小
 * @displayName hy-price
 */
defineOptions({});

// const props = withDefaults(defineProps<IProps>(), defaultProps)
const props = defineProps({
  /** 传入金额值 */
  text: [String, Number] as PropType<string | number>,
  /** 金额符号 */
  symbol: {
    type: String,
    default: "￥",
  },
  /** 比例大小 */
  ratio: {
    type: Number,
    default: 1.4,
  },
  /** 保留小数点后几位数 */
  num: {
    type: Number,
    default: 2,
  },
  /** 字体颜色 */
  color: {
    type: String,
    default: "#FE3232",
  },
  /** 字体大小 */
  size: {
    type: [Number, String],
    default: 12,
  },
  /** 字体粗细 */
  weight: {
    type: Number,
    default: 500,
  },
  /** 是否倾斜 */
  slant: {
    type: Boolean,
    default: false,
  },
  /** 定义需要用到的外部样式 */
  customStyle: {
    type: Object as PropType<CSSProperties>,
    default: () => {},
  },
  /** 自定义外部类名 */
  customClass: String,
});
const emit = defineEmits(["click"]);

/**
 * @description 价格整体样式
 * */
const priceStyle = computed<CSSProperties>(() => {
  const style: CSSProperties = {
    color: props.color,
    fontWeight: props.weight,
    fontStyle: props.slant ? "oblique" : "",
    fontSize: addUnit(props.size),
  };

  return Object.assign(style, props.customStyle);
});

/**
 * @description 价格处理
 * */
const priceOne = computed(() => {
  if (props.text === undefined) return error("text值不能为空");

  let value =
    typeof props.text === "string" ? props.text : props.text.toString();
  if (/\./g.test(value)) {
    return Number(value) ? value.split(".") : ["0", "000000"];
  } else {
    return [value, "000000"];
  }
});

/**
 * @description 点击事件
 * */
const handleClick = () => {
  emit("click", props.text);
};
</script>

<style lang="scss" scoped>
@import "./index.scss";
</style>
